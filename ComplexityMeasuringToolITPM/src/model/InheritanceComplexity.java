package model;

public class InheritanceComplexity {

	private String line = "";
	private int ci = 0;
	private int inditect = 0;
	private int direct = 0;
	private int total = 0;
	
	public String getLine() {
		return line;
	}
	public void setLine(String line) {
		this.line = line;
	}
	public int getCi() {
		return ci;
	}
	public void setCi(int ci) {
		this.ci = ci;
	}
	public int getInditect() {
		return inditect;
	}
	public void setInditect(int inditect) {
		this.inditect = inditect;
	}
	public int getDirect() {
		return direct;
	}
	public void setDirect(int direct) {
		this.direct = direct;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
	
}
