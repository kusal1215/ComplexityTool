package model;

public class VariableComplexity {

	private String line = "";
	private int complexity = 0;
	private int wvs = 0;
	private int primitive_variable = 0;
	private int composite_variable = 0;
	
	
	public String getLine() {
		return line;
	}
	public void setLine(String line) {
		this.line = line;
	}
	public int getComplexity() {
		return complexity;
	}
	public void setComplexity(int complexity) {
		this.complexity = complexity;
	}
	public int getWvs() {
		return wvs;
	}
	public void setWvs(int wvs) {
		this.wvs = wvs;
	}
	public int getPrimitive_variable() {
		return primitive_variable;
	}
	public void setPrimitive_variable(int primitive_variable) {
		this.primitive_variable = primitive_variable;
	}
	public int getComposite_variable() {
		return composite_variable;
	}
	public void setComposite_variable(int composite_variable) {
		this.composite_variable = composite_variable;
	}
	
	
	
}
