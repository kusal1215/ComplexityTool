package model;

/**
 * @author oshadi
 *
 */
public class Recursive {
	
	String FileName;
	String className;
	String RecursiveName;
	/**
	 * @return the fileName
	 */
	public String getFileName() {
		return FileName;
	}
	/**
	 * @param fileName the fileName to set
	 */
	public void setFileName(String fileName) {
		FileName = fileName;
	}
	/**
	 * @return the className
	 */
	public String getClassName() {
		return className;
	}
	/**
	 * @param className the className to set
	 */
	public void setClassName(String className) {
		this.className = className;
	}
	/**
	 * @return the recursiveName
	 */
	public String getRecursiveName() {
		return RecursiveName;
	}
	/**
	 * @param recursiveName the recursiveName to set
	 */
	public void setRecursiveName(String recursiveName) {
		RecursiveName = recursiveName;
	}
	
	

}
